            <h2>PROJECT MANAGEMENT</h2>
            <br><br>
            <p>
            Project management is a critical activity in any company:
            <br><br>
            •  A project often involves significant resources (financial and human). <br>
            •  It involves major, even strategic issues<br>
            •  Its objectives, duration and management are very different from the day-to-day operations of the company's internal teams.
            We help and support organizations with their projects, in two main areas :<br>
            •  Industrial projects: new equipment, workshops, organizations, logistics or production sites, etc.<br>
            •  MES (Manufacturing Execution System) projects: assistance with tool selection, IS integration, parameterization, project follow-up, etc.<br>
            We can handle all or part of the following areas:<br><br>
            •  Project management assistance to management<br>
            •  Carrying out a simulation to define the investment plan<br>
            •  Proposal of financing scenarios and advice on financial arrangements<br>
            •  We offer assistance and support in selecting specialized engineering firms.<br>
            •  Carrying out economic and financial studies: NPV, IRR and IP as decision-making aids (integrating the results of specific studies).<br>
            •  Training and project methodology for internal teams (role of PMO: Project Management Office).<br>
            •  Project monitoring (schedule, budget, compliance with requirements, steering committees, etc.)<br>
            •  Change management (studies, training, communication, etc.)<br>
            •  On-site support (site meetings, action monitoring, operational management, etc.)<br>

